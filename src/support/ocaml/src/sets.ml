(* -------------------------------------------------------------------- *)
module Impl = BatSet.PSet

(* -------------------------------------------------------------------- *)
type 'a set = 'a Impl.t

(* -------------------------------------------------------------------- *)
let empty () : 'a set =
  Impl.create Pervasives.compare

(* -------------------------------------------------------------------- *)
let add (x : 'a) (s : 'a set) : 'a set =
  Impl.add x s

(* -------------------------------------------------------------------- *)
let remove (x : 'a) (s : 'a set) : 'a set =
  Impl.remove x s

(* -------------------------------------------------------------------- *)
let mem (x : 'a) (s : 'a set) : bool =
  Impl.mem x s

(* -------------------------------------------------------------------- *)
let count (s : 'a set) : int =
  Impl.cardinal s
